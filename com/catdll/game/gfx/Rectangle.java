package com.catdll.game.gfx;

public class Rectangle 
{
    public float x;
    
    public float y;

    public float width;

    public float height;

    public Rectangle(float x, float y, float width, float height)
    {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
